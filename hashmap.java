import java.util.hashmap;

public class hashmap {
    public static void main(String[] args) {
        hashmap<String, Integer> map = new hashmap<>();
        map.put("Satu", 1);
        map.put("Dua", 2);
        map.put("Tiga", 3);
        map.put("Empat", 4);

        System.out.println("Isi HashMap: " + map);

    }
}
